def set_language(language: str) -> None:
    global _current_language
    _current_language = language if language in TRANSLATIONS else DEFAULT_LANGUAGE


def get_language() -> str:
    return _current_language


def t(key: str) -> str:
    return TRANSLATIONS.get(_current_language, TRANSLATIONS[DEFAULT_LANGUAGE]).get(key, key)


def tr_team(name: str) -> str:
    return TEAM_TRANSLATIONS.get(_current_language, TEAM_TRANSLATIONS[DEFAULT_LANGUAGE]).get(
        name, name
    )


def tr_agent(name: str) -> str:
    return AGENT_TRANSLATIONS.get(
        _current_language, AGENT_TRANSLATIONS[DEFAULT_LANGUAGE]
    ).get(name, name)


def tr_status(status: str) -> str:
    return STATUS_TRANSLATIONS.get(
        _current_language, STATUS_TRANSLATIONS[DEFAULT_LANGUAGE]
    ).get(status, status)
